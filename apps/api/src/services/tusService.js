// apps/api/src/services/tusService.js
//
// Builds a tus Server instance for a given gallery.
//
// Upload flow:
//   1. Client sends tus POST (create) with Upload-Metadata: filename, galleryId, userId
//   2. Server stores chunks under internal/tus/<galleryId>/
//   3. On complete (onUploadFinish):
//      a. Move file to private/<slug>/photos/<uuid>.<ext>
//      b. Run Sharp validation + SM thumbnail (same as multer pipeline)
//      c. INSERT into photos table (ON DUPLICATE KEY skips duplicates)
//      d. Enqueue MD thumbnail + prerender
//      e. Extract EXIF async
//      f. Mark gallery needs_rebuild
//
// The completed upload ID is the tus upload ID (UUID generated by @tus/server).
// The photo row ID is derived the same way as the multer flow: UUID = filename without ext.

