const _SUPPORTED = ['en', 'fr', 'de', 'it', 'es', 'pt'];

/** Pick best locale from a priority list, falling back to browser then 'en'. */
window.gpDetectLocale = function(...candidates) {
  const browser = (navigator.language || '').slice(0, 2).toLowerCase();
  for (const c of [...candidates, browser, 'en']) {
    if (c && _SUPPORTED.includes(c)) return c;
  }
  return 'en';
};

/** Translate a key. Vars: t('x.y', {n:3}) replaces {n}. Falls back to EN then the key. */
window.t = function(key, vars) {
  const locale = window.GP_LOCALE || 'en';
  let s = (_T[locale] || {})[key] || (_T.en || {})[key] || key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
  return s;
};

/**
 * Apply translations to the DOM.
 * [data-i18n]             → textContent
 * [data-i18n-html]        → innerHTML
 * [data-i18n-placeholder] → placeholder attribute
 * [data-i18n-title]       → title attribute
 */
window.applyI18n = function() {
  document.documentElement.lang = window.GP_LOCALE || 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  document.querySelectorAll('[data-i18n-html]').forEach(el => { el.innerHTML = t(el.dataset.i18nHtml); });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => { el.placeholder = t(el.dataset.i18nPlaceholder); });
  document.querySelectorAll('[data-i18n-title]').forEach(el => { el.title = t(el.dataset.i18nTitle); });
};
